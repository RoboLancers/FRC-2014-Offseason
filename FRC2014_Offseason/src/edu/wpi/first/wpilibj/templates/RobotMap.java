package edu.wpi.first.wpilibj.templates;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
    
    //Drive Motors
    public static final int KdriveMotor1 = 1;
    public static final int KdriveMotor2 = 2;
    public static final int KdriveMotor3 = 3;
    public static final int KdriveMotor4 = 4;
    public static final int KdriveMotor5 = 5;
    public static final int KdriveMotor6 = 6;
    
    //roller motors
    public static final int kFrontMotor = 7;
    public static final int kRearMotor = 8;
    
    
    //Solenoids
    public static final int KdriveSolenoidFwd = 7;
    public static final int KdriveSolenoidRev = 8;
    public static final int kFrontRollerFwd = 1;
    public static final int kFrontRollerRev = 2;
    public static final int kRearRollerFwd = 3;
    public static final int kRearRollerRev = 4; 
    
    public static final int Kcompressor = 8;
    public static final int KpressureSwitch = 14;
    public static final int kGyro = 1;
    
    
    
    
    
}
